<?php

namespace App\Model;

use Nelmio\ApiDocBundle\Annotation\Model;


class Response400
{
    STATIC $DEFAULT_GENERIC_ERROR = "Generic error.";


	protected $message;


	protected $error;

    /**
     * @return mixed
     */
    public function getMessage()
    {
        return $this->message;
    }

    /**
     * @param mixed $message
     */
    public function setMessage($message): void
    {
        $this->message = $message;
    }

    /**
     * @return mixed
     */
    public function getError()
    {
        return $this->error;
    }

    /**
     * @param mixed $error
     */
    public function setError($error): void
    {
        $this->error = $error;
    }


}
